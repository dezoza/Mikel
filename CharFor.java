package For;

public class CharFor 
{
	
	
	public static void main(String[] args) 
	{
		
		System.out.println("***Small Letter***");
		for(char c='a'; c<='z'; c++)
		{
			System.out.println(c);
		}
		System.out.println("***Caps***");
		for(int i = 'A' ; i <= 'Z'; i++) 
	    {
	      System.out.println(i);  
	    }
	}
}
