package If;

public class ElseIfTest 
{
	public static void main(String[] args) {
		int Time = 18;
		if (Time<10)
		{
			System.out.println("Greetings:::GoodMorning");
		}
		else if (Time<15) 
		{
			System.out.println("Greetings:::GoodAfterNoon");
		}
		else
		{
			System.out.println("Greetings:::GoodEvening");
		}
	}
}
